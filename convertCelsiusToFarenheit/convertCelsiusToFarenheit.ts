function convertCelsiusToFarenheit(celsius: number): number {
    return celsius * 9 / 5 + 32;
}

console.log(convertCelsiusToFarenheit(-20)); // DEVUELVE -4
console.log(convertCelsiusToFarenheit(40)); // DEVUELVE 104
console.log(convertCelsiusToFarenheit(5)); // DEVUELVE 41
console.log(convertCelsiusToFarenheit(-30)); // DEVUELVE -22
